from PIL import Image, ImageDraw

def compare_image_and_bbox(image_path, bbox_points):
    # Load the PIL image
    img = Image.open(image_path)

    # Create a copy of the image to draw the bounding box
    img_with_bbox = img.copy()
    draw = ImageDraw.Draw(img_with_bbox)

    # Draw the bounding box on the image
    # for x1, y1, x2, y2 in bbox_points:
        # draw.rectangle((x1, y1, x2, y2), outline="red", width=2)

    for p in bbox_points:
        draw.rectangle(p, outline="red", width=2)

    # Display the original image with the bounding box
    img.show()
    img_with_bbox.show()

    # Save the images if needed
    img_with_bbox.save("image_with_bbox.jpg")

    # Close the images
    img.close()
    img_with_bbox.close()

